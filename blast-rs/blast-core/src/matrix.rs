//! Scoring matrices: BLOSUM45, BLOSUM50, BLOSUM62, BLOSUM80, BLOSUM90, PAM30, PAM70, PAM250.
//!
//! Matrices are indexed by Ncbistdaa residue codes (0-27).
//! Row/column order follows the standard Ncbistdaa table.

use std::str::FromStr;
use thiserror::Error;

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum MatrixType {
    Blosum45,
    Blosum50,
    Blosum62,
    Blosum80,
    Blosum90,
    Pam30,
    Pam70,
    Pam250,
}

impl Default for MatrixType {
    fn default() -> Self { MatrixType::Blosum62 }
}

impl FromStr for MatrixType {
    type Err = String;
    fn from_str(s: &str) -> Result<Self, Self::Err> {
        match s.to_uppercase().as_str() {
            "BLOSUM45" => Ok(MatrixType::Blosum45),
            "BLOSUM50" => Ok(MatrixType::Blosum50),
            "BLOSUM62" => Ok(MatrixType::Blosum62),
            "BLOSUM80" => Ok(MatrixType::Blosum80),
            "BLOSUM90" => Ok(MatrixType::Blosum90),
            "PAM30"    => Ok(MatrixType::Pam30),
            "PAM70"    => Ok(MatrixType::Pam70),
            "PAM250"   => Ok(MatrixType::Pam250),
            _ => Err(format!("Unknown matrix: {}", s)),
        }
    }
}

/// Scoring matrix for protein alignment.
/// Internally stores a 28×28 score table indexed by Ncbistdaa codes.
#[derive(Debug, Clone)]
pub struct ScoringMatrix {
    pub name: MatrixType,
    /// scores[a][b] = score for aligning residue a with residue b
    pub scores: [[i32; 28]; 28],
    /// Lowest score (used for out-of-range residues)
    pub min_score: i32,
}

impl ScoringMatrix {
    pub fn new(name: MatrixType) -> Self {
        let raw = match name {
            MatrixType::Blosum62 => &BLOSUM62_RAW,
            MatrixType::Blosum45 => &BLOSUM45_RAW,
            MatrixType::Blosum50 => &BLOSUM50_RAW,
            MatrixType::Blosum80 => &BLOSUM80_RAW,
            MatrixType::Blosum90 => &BLOSUM90_RAW,
            MatrixType::Pam30    => &PAM30_RAW,
            MatrixType::Pam70    => &PAM70_RAW,
            MatrixType::Pam250   => &PAM250_RAW,
        };
        let mut scores = [[i32::MIN / 2; 28]; 28];
        let mut min_score = 0i32;
        // Fill from the raw 24×24 table (ACDEFGHIKLMNPQRSTVWYBZX*)
        // Map to Ncbistdaa indices
        for (i, &ri) in MATRIX_ORDER.iter().enumerate() {
            for (j, &rj) in MATRIX_ORDER.iter().enumerate() {
                let s = raw[i * MATRIX_ORDER.len() + j];
                scores[ri as usize][rj as usize] = s;
                if s < min_score { min_score = s; }
            }
        }
        ScoringMatrix { name, scores, min_score }
    }

    #[inline]
    pub fn score(&self, a: u8, b: u8) -> i32 {
        let ai = a as usize;
        let bi = b as usize;
        if ai < 28 && bi < 28 {
            self.scores[ai][bi]
        } else {
            self.min_score
        }
    }
}

