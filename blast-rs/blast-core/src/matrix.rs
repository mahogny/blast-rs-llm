// Nucleotide scoring: reward/penalty model
pub struct NucleotideScoring {
    pub match_score: i32,
    pub mismatch_score: i32,
}

impl Default for NucleotideScoring {
    fn default() -> Self {
        NucleotideScoring { match_score: 2, mismatch_score: -3 }
    }
}

/// Table: ASCII nucleotide → 2-bit code (A=0, C=1, G=2, T=3, other=4)
pub fn nt_to_2bit(c: u8) -> u8 {
    match c.to_ascii_uppercase() {
        b'A' => 0,
        b'C' => 1,
        b'G' => 2,
        b'T' | b'U' => 3,
        _ => 4, // ambiguous
    }
}

